// Bit Operations Demo Program

// Initialize values
mov a 0xCC    // A = 0xCC (binary: 11001100)
mov b 0xAA    // B = 0xAA (binary: 10101010)

// Store original values for reference
store a 0x80  // [0x80] = A
store b 0x81  // [0x81] = B

// Print all results
print 0x80    // Original A (0xCC)
print 0x81    // Original B (0xAA)

// End of program
halt